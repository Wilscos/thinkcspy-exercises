# This program lets you compute the area of a rectangle by entering its width and height.

width = float(input("Enter the width of the triangle (in numbers and cm): "))
height = float(input("Enter the height of the triangle (in numbers and cm): "))

areaRectangle = width * height
print("The area of the triangle with width of", width, "cm and height of", height, "cm is", areaRectangle, "cm^2.")
